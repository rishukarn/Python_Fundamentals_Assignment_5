# Write a Python program to demonstrate string slicing.
str1='Hello world'
print(str1[:5])
print(str1[2:])
print(str1[1:4])
print(str1[1:5:2])
print(str1[::2])
print(str1[::-1])
print(str1[:-2])
print(str1[-3:])





